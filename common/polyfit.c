double polyfit(double x, const polyfactors *fac)
{
    size_t i;
    double result = 0.0;
    const double *factors = &fac->x0;

    for (i = 0; i <= 10; i++) {
        if (factors[i]) {
            result += factors[i] * pow(x, (double)i);
        }
    }
    result = MAX(result, fac->y_min);
    result = MIN(result, fac->y_max);
    return roundl(result);
}

